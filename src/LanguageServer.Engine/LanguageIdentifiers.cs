        ///     The identifier for the VS Solution XML language.
        /// </summary>
        public static readonly string VsSolutionXml = "slnx";

        /// <summary>
        ///     The identifier for the XML language.
        /// </summary>
        public static readonly string Xml = "xml";
    }
}

